package com.tnu.basic;

public class NestedIf {

	public static void main(String[] args) 
	{
		int a=30,y=10;
		if(a==30)
		{
			
			 if(y==10)
			{
				System.out.println("a=30 and y=10");
			}
			 else {
				 System.out.println("value of y is not equal to 10");
			 }
		
		}
		else
		{
			System.out.println("value of a is not equal to 30");
		}
		

	}

}
